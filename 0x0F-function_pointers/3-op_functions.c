/**
* op_add - sum function
* @a: parameter a
* @b: parameter b
* Return: a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - diff of a and b
* @a: parameter a
* @b: parameter b
* Return: a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - product of a and b
* @a: parameter a
* @b: parameter b
* Return: a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - division of a by b
* @a: parameter a
* @b: parameter b
* Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - remainder of the division of a by b
* @a: parameter a
* @b: parameter b
* Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
